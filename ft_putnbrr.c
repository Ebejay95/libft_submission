#include "libft.h"

int	ft_putnbrr(int n)
{
	char	nbr;
	int		r;

	r = 0;
	if (n == -2147483648)
	{
		r += write(1, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		n = n * (-1);
		r += write(1, "-", 1);
	}
	nbr = (n % 10) + '0';
	n = n / 10;
	if (n > 0)
		r += ft_putnbrr(n);
	r += write(1, &nbr, 1);
	return (r);
}
